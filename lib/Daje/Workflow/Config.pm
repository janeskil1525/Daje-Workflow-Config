our $VERSION = "0.01";

use Daje::Config;

has 'path' => "";
has 'config' => {};
has 'error' => '';

sub load($self, $filename) {

    try {
        my $config = Daje::Config->new(
            path => $self->path,
        )->load($filename);

        $self->config($config);
    } catch($e) {
        $self->error($e);
    };

    return $self;
}

sub param($self, $parameter) {
    my $result = "";

    try {
        $result = $self->config->{$parameter};
    } catch ($e) {
        $self->error($e);
    };

    return $result;
}


1;
